public class inchMM {
    public static void main(String[] args) {
        double CONV = 25.4;
        double inch = 2;
        double inch2 = 5;
        double inch3 = 10;

        System.out.println("2 inches = " +inch*CONV+"mm");
        System.out.println("5 inches = " +inch2*CONV+"mm");
        System.out.println("10 inches = " +inch3*CONV+"mm");
    }
}
